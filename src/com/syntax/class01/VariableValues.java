package com.syntax.class01;

public class VariableValues {

	public static void main(String[] args) {
		byte variable1 = 15;
		// Updated Value
		variable1 = 116;
		System.out.println(variable1);

		short variable2 = 1234;
		// Updated Value
		variable2 = 3458;
		System.out.println(variable2);

		int variable3 = 76546;
		// Updated Value
		variable3 = 4567;
		System.out.println(variable3);

		long variable4 = 123456789;
		// Updated Value
		variable4 = 5;
		System.out.println(variable4);

		float variable5 = 123.123f;
		// Updated Value
		variable5 = 23.6f;
		System.out.println(variable5);

		double variable6 = 12345.12345;
		// Updated Value
		variable6 = 44.4;
		System.out.println(variable6);

		char variable7 = 'F';
		// Updated Value
		variable7 = 'R';
		System.out.println(variable7);

		boolean rain = true;
		boolean sunny = false;
		// Updated Value
		rain = false;
		sunny = true;
		System.out.println(rain);
		System.out.println(sunny);
	}

}
